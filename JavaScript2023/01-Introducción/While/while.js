// El ciclo While es similar al For, pero con un solo condicional, aunque no se utiliza mucho
// Generalmente, fuera del ciclo While hay una variable y se incrementa dentro
let contador = 0;
while (contador <= 10){
    console.log(contador);
    contador++;
}
